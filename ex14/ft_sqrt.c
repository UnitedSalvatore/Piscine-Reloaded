int		ft_sqrt(int nb)
{
	int m;
	int l;
	int u;

	l = 0;
	u = 46340;
	while (l <= u)
	{
		m = (u + l) / 2;
		if ((m * m) == nb)
			return (m);
		else if ((m * m) > nb)
			u = m - 1;
		else
			l = m + 1;
	}
	return (0);
}
